class Solution {
    public Node addOne(Node head) {
        // code here.
        head=reverseList(head);
        Node curr=head;
        int sum=curr.data+1;
        int carry=0;
        Node prev=null;
        while(curr!=null){
            carry=sum/10;
            sum=sum%10;
            curr.data=sum;
            prev=curr;
            curr=curr.next;
            if(curr!=null){
                sum=carry+curr.data;
            }
        }
        if(carry==1){
            prev.next=new Node(1);
        }
        head=reverseList(head);
        return head;
    }
    public static Node reverseList(Node head){
        Node curr=head;
        Node prev=null;
        while(curr!=null){
            Node next=curr.next;
            curr.next=prev;
            prev=curr;
            curr=next;
        }
        return prev;
    }
}
